use num::integer::{gcd, lcm};
use proconio::{
    fastout, input,
    marker::{Chars, Usize1},
};

macro_rules! debug {
    ($($var:expr),+) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($(stringify!($var), "={:?}  "),+), $(&$var),+);
    };
}

#[fastout]
fn solve() {
    input! {
        S: Chars,
    }
    debug!(S);

    let mut stacks = vec![];
    for i in 1..=S.len() {
        if S[i - 1] == '(' {
            stacks.push(i);
        } else {
            println!("{} {}", stacks[stacks.len() - 1], i);
            stacks.pop();
        }
    }
}

fn main() {
    Builder::new()
        .stack_size(32 * 1024 * 1024) // default: 2MiB -> 32MiB
        .spawn(solve)
        .unwrap()
        .join()
        .unwrap();
}
