use num::integer::{gcd, lcm};
use proconio::{
    fastout, input,
    marker::{Chars, Usize1},
};

macro_rules! debug {
    ($($var:expr),+) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($(stringify!($var), "={:?}  "),+), $(&$var),+);
    };
}

#[fastout]
fn solve() {
    input! {
        D: usize,
        N: usize,
        LRH: [(Usize1, Usize1, usize); N],
    }
    debug!(D, N, LRH);

    let mut limits = vec![24; D];
    for (l, r, h) in LRH.into_iter() {
        for i in l..=r {
            limits[i] = limits[i].min(h);
        }
    }
    debug!(limits);

    println!("{}", limits.iter().sum::<usize>());
}

fn main() {
    Builder::new()
        .stack_size(32 * 1024 * 1024) // default: 2MiB -> 32MiB
        .spawn(solve)
        .unwrap()
        .join()
        .unwrap();
}
