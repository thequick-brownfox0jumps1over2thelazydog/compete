use proconio::{
    fastout, input,
    marker::{Chars, Usize1},
};

macro_rules! debug {
    ($($var:expr),+) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($(stringify!($var), "={:?}  "),+), $(&$var),+);
    };
}

#[fastout]
fn solve() {
    input! {
        N: usize,
        S: Chars,
    }
    debug!(N, S);

    let mut previous_color = 'W';
    let mut count = 1;
    for &s in S.iter() {
        if s != previous_color {
            previous_color = s;
            count = 1;
            continue;
        }

        count += 1;
        if count == 3 {
            println!("Yes");
            return;
        }
    }

    println!("No");
}

fn main() {
    Builder::new()
        .stack_size(32 * 1024 * 1024) // default: 2MiB -> 32MiB
        .spawn(solve)
        .unwrap()
        .join()
        .unwrap();
}
