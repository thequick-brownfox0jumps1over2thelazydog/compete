macro_rules! debug {
    ($($var:expr),+) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($(stringify!($var), "={:?}  "),+), $(&$var),+);
    };
}

#[fastout]
fn solve() {
    input! {
        N: usize,
        mut A: [[usize; N]; N],
        Q: usize,
        Queries: [(usize, Usize1, Usize1); Q],
    }
    debug!(N, A, Q, Queries);

    let mut rows = (0..N).collect::<Vec<usize>>();

    for &(query_type, i, j) in Queries.iter() {
        if query_type == 1 {
            rows.swap(i, j);
            continue;
        }

        println!("{}", A[rows[i]][j]);
    }
}

fn main() {
    Builder::new()
        .stack_size(32 * 1024 * 1024) // default: 2MiB -> 32MiB
        .spawn(solve)
        .unwrap()
        .join()
        .unwrap();
}
