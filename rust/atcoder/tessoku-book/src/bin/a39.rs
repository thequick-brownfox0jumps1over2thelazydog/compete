use itertools::Itertools;
use num::integer::{gcd, lcm};
use proconio::{
    fastout, input,
    marker::{Chars, Usize1},
};

macro_rules! debug {
    ($($var:expr),+) => {
        #[cfg(debug_assertions)]
        eprintln!(concat!($(stringify!($var), "={:?}  "),+), $(&$var),+);
    };
}

#[fastout]
fn solve() {
    input! {
        N: usize,
        mut LR: [(usize, usize); N],
    }
    debug!(N, LR);

    LR.sort_by(|a, b| a.1.cmp(&b.1).then(a.0.cmp(&b.0)));
    debug!(LR);

    let mut result = 0;
    let mut time = 0;
    for (l, r) in LR.into_iter() {
        if l >= time {
            time = r;
            result += 1;
        }
    }

    println!("{result}");
}

fn main() {
    Builder::new()
        .stack_size(32 * 1024 * 1024) // default: 2MiB -> 32MiB
        .spawn(solve)
        .unwrap()
        .join()
        .unwrap();
}
